package bolum01.alıştırmalar;

public class B01A13 {

	public static void main(String[] args) {
		// Problemdeki x ve y degerlerini kullanarak determinantı (D) bulun.
		System.out.println("D: " + ((3.4 * 0.55) + (2.1 * 50.2)));
		
		/*İlk sutunndaki x degerlerini asagıdaki degerlerle degistirirsek determinantı(Dx) bulun
		  Esittir isaretindencsonra y sutununu degistirmeden bırakınız*/
		
		
		System.out.println("Dx: " + ((44.5 * 0.55) + (5.9 * 50.2)));
		
		/*İkinci sutundaki y degerlerini asagıdaki degerlerle degistirerek determinantı Dy yi bulun
		 esittir isaretinden sonra x sutununu degistirmeden bırakınız*/
		
		
		System.out.println("Dy: " + ((3.4 * 5.90) + (2.1 * 44.5)));
		
		// x ve y degerlerini bulmak icin Cramer kuralını kullanın
		
		System.out.println("(x,y) = " + "(" + (320.655 / 107.29) + "," + (113.51 / 107.29) + ")");
		 
		 
		
        
	}

}
